import { test, expect, Page } from '@playwright/test';

// ────────────────────────────────────────────────────────────────
// 共通処理：ログイン画面を開く
//
// playwright.config.ts に baseURL = 'http://localhost:8082/atrs/' が設定されているため、
// page.goto('auth/login?form') は http://localhost:8082/atrs/auth/login?form に遷移する。
//
// goto の直後に URL を確認しているのは「画面が正しく表示された状態」でテストを始めるため。
// この確認が失敗した場合は、ATRS は起動しているが期待と異なる URL にリダイレクトされている可能性がある。
// ATRS 自体が起動していない場合は、page.goto() の時点で接続エラーになりこの行には到達しない。
// ────────────────────────────────────────────────────────────────
async function gotoLoginPage(page: Page) {
  await page.goto('auth/login?form');
  await expect(page).toHaveURL(/\/auth\/login/);
}

// ────────────────────────────────────────────────────────────────
// 共通処理：ログインフォームに入力してログインボタンをクリックする
//
// 引数に空文字列を渡した場合は fill をスキップする。
// これにより「会員番号 未入力」「パスワード 未入力」のテストを同じ関数で表現できる。
//
// セレクタ（#membershipNumber, #password, #login-btn）は
// ATRS のログイン画面 HTML の id 属性に対応している。
// ────────────────────────────────────────────────────────────────
async function submitLoginForm(page: Page, membershipNumber: string, password: string) {
  if (membershipNumber) await page.fill('#membershipNumber', membershipNumber);
  if (password)         await page.fill('#password', password);
  await page.click('#login-btn');
}

// ════════════════════════════════════════════════════════════════
// ログイン画面 (A101) 状態遷移テスト
//
// テストベース：課題5で作成した状態遷移図（カバレッジアイテム T1〜T3）
// 各テストケース（TC-L-001〜005）はカバレッジアイテムの遷移を1件ずつ確認する。
// ════════════════════════════════════════════════════════════════
test.describe('ログイン画面 (A101) - 状態遷移テスト', () => {

  // 各テストの開始前にログイン画面を開く。
  // beforeEach を使うことで、テストごとに画面をリセットした状態から始められる。
  test.beforeEach(async ({ page }) => {
    await gotoLoginPage(page);
  });

  // ────────────────────────────────────────────────────────────────
  // TC-L-001: 正常ログイン
  // カバレッジアイテム T1：未ログイン → [入力情報が正しい] → ログイン済み
  //
  // 確認すること：
  //   - TOP画面（/ticket/search）へ遷移すること
  //   - ヘッダに「ようこそ」が表示されること（ログイン状態の証拠）
  // ────────────────────────────────────────────────────────────────
  test('TC-L-001: 正常ログイン', async ({ page }) => {
    await submitLoginForm(page, '0000000001', 'aaaaa11111');

    // 正常ログイン後は TOP画面（チケット検索画面）へ遷移する
    await expect(page).toHaveURL(/\/ticket\/search/);

    // ヘッダの「ようこそ」表示でログイン状態を確認する
    await expect(page.getByText('ようこそ')).toBeVisible();
  });

  // ────────────────────────────────────────────────────────────────
  // TC-L-002: クライアントエラー（会員番号 未入力）
  // カバレッジアイテム T2：未ログイン → [入力情報に誤りあり] → 未ログイン
  //
  // 確認すること：
  //   - ログイン画面に留まること（サーバーへリクエストは送られない）
  //   - クライアントエラー「入力必須項目です。」が表示されること
  //     （ブラウザのバリデーション（Parsley.js）による表示）
  // ────────────────────────────────────────────────────────────────
  test('TC-L-002: クライアントエラー（会員番号 未入力）', async ({ page }) => {
    await submitLoginForm(page, '', 'aaaaa11111');

    // クライアントエラーのためページ遷移は起きず、ログイン画面の URL のまま
    await expect(page).toHaveURL(/\/auth\/login/);

    // ブラウザのバリデーション（Parsley.js）によるエラーメッセージを確認する
    await expect(page.getByText('入力必須項目です。')).toBeVisible();
  });

  // ────────────────────────────────────────────────────────────────
  // TC-L-003: クライアントエラー（パスワード 未入力）
  // カバレッジアイテム T2：未ログイン → [入力情報に誤りあり] → 未ログイン
  //
  // TC-L-002 と同じカバレッジアイテムだが、未入力の項目が異なる。
  // 確認すること：TC-L-002 と同じ
  // ────────────────────────────────────────────────────────────────
  test('TC-L-003: クライアントエラー（パスワード 未入力）', async ({ page }) => {
    await submitLoginForm(page, '0000000001', '');

    await expect(page).toHaveURL(/\/auth\/login/);
    await expect(page.getByText('入力必須項目です。')).toBeVisible();
  });

  // ────────────────────────────────────────────────────────────────
  // TC-L-004: 認証エラー（会員番号 不存在）
  // カバレッジアイテム T3：未ログイン → [会員番号なし / パスワード誤り] → 未ログイン
  //
  // 確認すること：
  //   - サーバーへリクエストが送られ /auth/dologin の URL になること
  //   - サーバーからのエラーメッセージ（.alert）が表示されること
  //
  // TC-L-002/003（クライアントエラー）との違い：
  //   クライアントエラーはブラウザが弾くためサーバーへ届かない。
  //   認証エラーはサーバーまで届いた上で返ってくるエラーである。
  // ────────────────────────────────────────────────────────────────
  test('TC-L-004: 認証エラー（会員番号 不存在）', async ({ page }) => {
    await submitLoginForm(page, '9999999999', 'aaaaa11111');

    // 認証エラーはサーバー処理後にフォワードされるため URL が /auth/dologin になる
    await expect(page).toHaveURL(/\/auth\/dologin/);

    // サーバーから返るエラーメッセージ領域（.alert）が表示されることを確認する
    await expect(page.locator('.alert')).toBeVisible();
  });

  // ────────────────────────────────────────────────────────────────
  // TC-L-005: 認証エラー（パスワード 不一致）
  // カバレッジアイテム T3：未ログイン → [会員番号なし / パスワード誤り] → 未ログイン
  //
  // TC-L-004 と同じカバレッジアイテムだが、失敗の原因が異なる。
  // 「会員番号が存在しない場合」と「パスワードが違う場合」で
  // エラーメッセージが同一であることはセキュリティ上の仕様（どちらで失敗したか
  // 攻撃者に知らせないため）。ここでは表示されることだけを確認する。
  // ────────────────────────────────────────────────────────────────
  test('TC-L-005: 認証エラー（パスワード 不一致）', async ({ page }) => {
    await submitLoginForm(page, '0000000001', 'wrongpasswd');

    await expect(page).toHaveURL(/\/auth\/dologin/);
    await expect(page.locator('.alert')).toBeVisible();
  });
});
